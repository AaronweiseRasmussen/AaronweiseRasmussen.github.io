// Alert function to gather user name using alert() function. 

// Use window.prompt() to get an input.
var username = window.prompt("Enter Name Please");
// Use alert() to display the name.
alert(`Greetings, ${username}!`);


var myFavNum = window.prompt("Enter your Favorite Number:");

//stores a value that wont change.
const piValue = 3.1415926;

var myArea = parseInt(piValue) * parseInt(myFavNum) * parseInt(myFavNum);





alert(`Hello, ${username}! You entered ${myFavNum} as your FAVORITE number. 
Using that as the radius of a circle,
you would get ${myArea} as the Area.`);


